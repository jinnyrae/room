constituent pas arrhes dont l’abandon autoriserait les partis à se dégager du contrat.<br>
Dans le cas où il serait expressément stipulé que le matériel pourra être payé par
fractions, le vendeur pourra prendre un nantissement sur ledit matériel – les frais
