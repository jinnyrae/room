 Art. 5 - les paiements, même effectués au moyen d’effets de commerce domiciliés
en province ou à l’étranger, sont toujours réputés être faits à Paris.<br>
 Art. 6 - l’escompte de 2% peut être déduit du montant de cette facture pour
paiement sous 10 jours. Cet escompte sera déduit de notre chiffre d’affaires taxables,
le montant de la TVA déductible doit donc être diminué du montant de la TVA
