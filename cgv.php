 La déchéance du terme prend effet huit jours après l’envoi d’une lettre
recommandée de mise en demeure, restée sans effet.<br>
 Art. 9 - Si le vendeur ne fait pas jouer la clause de déchéance du terme, le retard
dans le règlement entraîne de plein droit le paiement d’intérêts de retard équivalent
à trois fois le taux de l’intérêt légal.<br>
 Tout frais de justice ou de recouvrement que pourrait avoir à engager le vendeur du
fait du défaut ou retard de paiement de l’acheteur devra être supporté en totalité
par ce dernier.
 Art. 10 - Toute créance, en principal et intérêt, est stipulée payable à paris.<br>
III - CLAUSE DE RÉSERVE DE PROPRIÉTÉ
 Art. 11 - D’un commun accord des parties, la présente vente est conclue sur la
clause suspensive du complet paiement du prix principal, intérêts et frais. <br>En cas de
cession à un sous-acquéreur, notre client devra obtenir une autorisation de notre
Société précisant le nom et l’adresse de ce sous-acquéreur et les modalités de
paiement consenties.
 Tant que le prix n’est pas payé, le vendeur reste propriétaire du matériel,
l’acquéreur étant simplement dépositaire. Toutefois, les risques sont transférés à
l’acquéreur dès la date de livraison.<br>
 Art. 12 - En cas de non-paiement à la date convenue, et huit jours après mise en
demeure restée infructueuse, le vendeur pourra reprendre le matériel après
autorisation de Monsieur le Président du tribunal statuant en référé, à moins que le
dépositaire ne le restitue spontanément.<br>
 Dans les deux cas, cependant, cette reprise est effectuée sans préjudice de
dommages-intérêts, contractuellement fixés à 1/3 de la valeur HT du matériel et une
indemnité d’utilisation égale à 5% de la valeur HT du matériel, par mois, du jour de la
livraison au jour de la restitution, étant expressément précisé que le dépositaire est
autorisé à utiliser le matériel.<br> Les acomptes versés s’imputeront sur le montant des
dommages-intérêts.
 Art. 13 - En tant que gardien détenteur, l’acquéreur assume, comme dit à l’Article
11, la responsabilité de tous préjudices ou dommages subis par le matériel ou causés
par lui, à des personnes ou à des biens, même s’ils sont intervenus à la suite d’un vice
de construction, ou un défaut de montage, même s’il s’agit d’un cas fortuit ou de
farce majeure.
