II - PRIX, CONDITIONS ET RETARD DE PAIEMENT
 Art. 4 - Les prix indiqués s’entendent sous réserve de toute modification, soit des
cours des changes, des conditions d’importation, des prix de nos fournisseurs.<br>
 Les prix définitifs, en baisse ou en hausse, seront ceux du tarif en cours au jour de la
livraison, même dans le cas où un acompte aura été versé à la commande. Dans ce
cas, cet acompte sera imputé sur le prix total résultant des clauses ci-dessus
