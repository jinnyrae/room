IV - LIVRAISON TRANSPORT DES MARCHANDISES
 Art. 14 - Les détails de livraison, ne sont donnés qu’à titre purement indicatifs et
sans engagement, même si des délais spéciaux ont été convenus. Le retard ou le
défaut de livraison ne peut en aucun cas donner lieu à dommages-intérêts ou
indemnité d’aucune sorte.<br>
 Art. 15 - Toutefois, si à l’expiration du délai prévu, le matériel n’était pas livré,
l’acheteur serait en droit de délivrer par lettre recommandée, une mise en demeure
d’avoir à livrer le matériel dans les trois mois.<br>
 A l’expiration de cette mise en demeure restée sans effet, la commende serait
annulée et les acomptes versés seraient remboursés.<br>
 Art. 16 - Les marchandises voyagent aux risques et périls du destinataire, même
quand les frais de port ont été avancés par le vendeur au départ.
 La clause de réserve de propriété existant sur les marchandises, dont le prix n’est
pas payé en totalité ne peut aucunement annuler le fait que l’acheteur ou le
destinataire doit supporter les risques et périls des marchandises transportées ou
expédiées.
V - GARANTIE
 Art. 17 - Le matériel livré est garanti, exclusivement dans les conditions définies à
l’Article 1643 du Code Civil, et ceci pour une durée journalière normale en une seule
équipe, pendant un délai de six mois. Il s’ensuit que le vendeur garantit le matériel
vendu contre tous vices cachés.<br>
 Art. 18 - Pour pouvoir invoquer le bénéfice de ces dispositions, l’acheteur doit
aviser le vendeur sans retard et par écrit des vices, qu’il impute au matériel et fournir
toutes justifications quant à la réalité de ceux-ci.<br>
 Art. 19 - La garantie est retirée dans les cas suivants :<br>
 - transformation de la machine
 - remplacement des pièces d’origine par des pièces d’autre provenance
 - changement de propriétaire de la machine
 - utilisation de la machine par une personne autre que l’acheteur ou l’un de ses
préposés
 - fournitures utilisées non conformes à celles indiquées ou remises lors de la
commande
 - déplacement du matériel spécial de son lieu d’utilisation d’origine sans le contrôle
de nos services
 - maintenance non conforme aux normes du constructeur.<br>
 Art. 20 - La garanties se borne à l’échange des pièces reconnues défectueuses par
nous ou à leur remise en état à notre convenance, les pièces étant livrées dans nos
usines ou succursales sans que nous ayons à participer en aucune façon aux frais de
main-œuvre occasionnés par ce démontage et ce remontage, ni aux frais et
conséquence de l’immobilisation du matériel.<br>
 Art. 21 - La réparation est effectuée en nos ateliers où la machine doit nous être
envoyée franco. Dans le cas où la réparation serait faite chez le client, celui-ci serait
tenu de régler la totalité des frais de dépannage et de séjour.
 Art. 22 - Les pièces incriminées doivent toujours être jointes à la réclamation. Les
échanges de pièces ou leur remise en état ne peuvent avoir pour effet de prolonger
la durée de la garantie.<br>
 Art. 23 - La garantie accordée ne peut motiver ni indemnité, ni dommages-intérêts.
Elle limite expressément la responsabilité du constructeur.<br>
 Art. 24 - La garantie exclut pour notre Société toute obligation de réparer les
dommages directs ou indirects résultant pour le client de l’utilisation du
fonctionnement des machines ou relatifs à cette utilisation ou à leur fonctionnent.
VI - VENTE DE PIÈCES DÉTACHÉES, ACCESSOIRES ET AIGUILLES
 Art. 25 - Les fournitures de pièces détachées, accessoires et aiguilles sont payables
comptant.
 Tout retard dans le paiement entraine l’exigibilité de la totalité du compte du client,
huit jours après mise en demeure par lettre recommandée restée infructueuse.<br>
 Ces factures ne donnent pas lieu à escompte. Tout retard dans le règlement
entraine de plein droit une pénalité égale à trois fois le taux de l’intérêt légal.<br>
