garantie.
 Art. 27 - Les délais de livraison des réparations sont donnés à titre indicatif, en
raison de la variété des circonstances qui peuvent déterminer les opérations de
réparation ou de transformation. En aucun cas, un retard de livraison ne peut donner
lieu à dommages-intérêts.<br>
 Art. 28 - Les travaux de réparation et de transformation sont payables comptant.
Tout retard dans le paiement entraine l’exigibilité de la totalité du compte client, huit
jours après mise en demeure restée infructueuse.<br>
 Ces factures ne donnent pas lieu à escompte. Tout retard dans le règlement
entraîne de plein droit une pénalité égale à trois fois le taux de l’intérêt légal.<br>
VIII - CONTESTATION ET CLAUSE ATTRIBUTIVE DE COMPÉTENCE
 Art. 29 - D’un commun accord entre les parties, il est prévu que toute contestation
ou litige concernant le présent contrat seront portés exclusivement devant les
