I – COMMANDE
 Art. 1 - Les commandes adressées par nos clients ne constituent une vente ferme
qu’après acceptation écrite de notre part, même en cas de versement d’un acompte.<br>
 Art. 2 - En cas de non-respect par l’acheteur d’une seule des conditions convenues
lors de la commande, celle-ci devient nulle de plein droit et l’acheteur devra verser, à
titre de clause pénale, des dommages-intérêts s’élevant à 15% du prix de vente TTC.
 Le montant de ces dommages-intérêts sera porté à 30% du prix TTC dans le cas où
la machine est spécialement importée à l’intention du client ou fabriquée selon les
normes et spécifications qui lui sont propres.<br>
Art. 3 - L’acquéreur reconnait que le fournisseur a intégralement assumé son
obligation de conseil et qu’il fait son affaire personnelle d’un usage autre que celui
