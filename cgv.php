 Art. 8 - tout retard dans le règlement de tout ou partie de prix entraîne, si bon
semble au vendeur, l’exigibilité de la totalité du solde dû.<br>
 Art. 9 - Depuis le 1er Janvier 2009, le délai convenu entre entreprises pour régler
